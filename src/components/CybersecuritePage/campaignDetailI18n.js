function looksLikeRawMicrosoftError(text) {
  const raw = String(text || "");
  return /AADSTS\d+/i.test(raw) || /"error_description"/.test(raw) || /Erreur d'authentification Microsoft/i.test(raw) || /Microsoft authentication failed/i.test(raw);
}

/**
 * Map structured / raw Microsoft sync errors to a localized user-facing toast.
 * @param {{ error?: string, errorCode?: string, details?: { tenantHint?: string|null } } | string | null} resultOrMessage
 * @param {ReturnType<typeof getCampaignDetailCopy>} copy
 */
export function formatMicrosoftSyncError(resultOrMessage, copy) {
  const auth = copy?.toasts?.microsoftAuth || {};
  const fallback = copy?.toasts?.syncError || auth.failed || "";
  const payload = typeof resultOrMessage === "string" ? {
    error: resultOrMessage
  } : resultOrMessage || {};
  const code = payload.errorCode || "";
  const raw = String(payload.error || "");
  const tenantHint = payload.details?.tenantHint || (() => {
    const match = raw.match(/tenant identifier '([^']+)'/i);
    return match?.[1] || null;
  })();
  const tenant = tenantHint ? ` (« ${tenantHint} »)` : "";

  const byCode = {
    MS_AUTH_INVALID_TENANT: auth.invalidTenant,
    MS_AUTH_INVALID_CLIENT: auth.invalidClient,
    MS_AUTH_INVALID_SECRET: auth.invalidSecret,
    MS_AUTH_UNAUTHORIZED: auth.unauthorized,
    MS_AUTH_CONFIG_MISSING: auth.configMissing,
    MS_AUTH_CONFIG_INCOMPLETE: auth.configIncomplete,
    MS_SYNC_CLIENT_REQUIRED: auth.clientRequired,
    MS_AUTH_FAILED: auth.failed,
    MS_SYNC_FAILED: fallback
  };

  if (code && byCode[code]) {
    return interpolate(byCode[code], {
      tenant
    });
  }

  if (/AADSTS900023|AADSTS90002|neither a valid DNS name|Tenant .+ not found/i.test(raw)) {
    return interpolate(auth.invalidTenant || fallback, {
      tenant
    });
  }
  if (/AADSTS7000215|AADSTS7000218|Invalid client secret/i.test(raw)) {
    return auth.invalidSecret || fallback;
  }
  if (/AADSTS700016|unauthorized_client|Application with identifier/i.test(raw)) {
    return auth.invalidClient || fallback;
  }
  if (looksLikeRawMicrosoftError(raw)) {
    return auth.failed || fallback;
  }
  return raw || fallback;
}
