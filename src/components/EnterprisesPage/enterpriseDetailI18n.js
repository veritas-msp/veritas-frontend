export function getPrestationCategoryLabel(category, locale) {
  const labels = getEnterpriseDetailCopy(locale).prestationCategories;
  if (labels[category]) return labels[category];
  const fallback = String(category || "")
    .replace(/^prestation-/, "")
    .replace(/-/g, " ")
    .trim();
  return fallback || "-";
}

